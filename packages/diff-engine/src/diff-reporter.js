'use strict';

const fs = require('fs-extra');
const path = require('path');

/**
 * DiffReporter — Generates diff reports in multiple formats
 *
 * Outputs:
 * - diff.json (machine-readable)
 * - DIFF-REPORT.md (human-readable)
 * - impact.json (impact assessment)
 *
 * @class DiffReporter
 * @version 1.0.0
 * @story 2.2
 */
class DiffReporter {
  constructor(options = {}) {
    this.outputPath = options.outputPath || process.cwd();
  }

  /**
   * Generate all reports
   * @param {Object} diff Diff from DiffAnalyzer
   * @param {Object} impact Impact from ImpactCalculator
   * @returns {Promise<Object>} Report file paths
   */
  async generateReports(diff, impact) {
    try {
      const jsonReport = await this._generateJSON(diff, impact);
      const mdReport = await this._generateMarkdown(diff, impact);
      const impactReport = await this._generateImpact(impact);

      return {
        json: jsonReport,
        markdown: mdReport,
        impact: impactReport,
        timestamp: new Date().toISOString(),
      };
    } catch (error) {
      throw new Error(`Report generation failed: ${error.message}`);
    }
  }

  /**
   * Generate JSON report
   * @private
   */
  async _generateJSON(diff, impact) {
    const reportPath = path.join(this.outputPath, 'diff.json');

    const report = {
      timestamp: new Date().toISOString(),
      baseline: diff.baseline,
      current: diff.current,
      changes: diff.changes,
      impact,
    };

    await fs.writeFile(reportPath, JSON.stringify(report, null, 2));
    return reportPath;
  }

  /**
   * Generate Markdown report
   * @private
   */
  async _generateMarkdown(diff, impact) {
    const reportPath = path.join(this.outputPath, 'DIFF-REPORT.md');
    const changes = diff.changes || {};

    const sections = [
      '# 📊 Repository Change Report\n\n',
      `**Baseline:** ${new Date(diff.baseline.timestamp).toLocaleDateString()}\n`,
      `**Current:** ${new Date(diff.current.timestamp).toLocaleDateString()}\n\n`,

      '## Impact Summary\n',
      `**Overall Score:** ${impact.overallScore}/10\n`,
      `**Severity:** ${this._severityBadge(impact.severity)}\n`,
      `**Breaking Change:** ${impact.breaking ? '❌ Yes' : '✅ No'}\n\n`,

      '## 📈 Key Metrics\n',
      '| Metric | Before | After | Change | Status |\n',
      '|--------|--------|-------|--------|--------|\n',
      `| Files | ${diff.baseline.files} | ${diff.current.files} | ${this._formatChange(diff.current.files - diff.baseline.files)} | ${diff.current.files >= diff.baseline.files ? '📈' : '📉'} |\n`,
      `| LOC | ${diff.baseline.loc} | ${diff.current.loc} | ${this._formatChange(diff.current.loc - diff.baseline.loc)} | ${diff.current.loc >= diff.baseline.loc ? '📈' : '📉'} |\n`,
      `| Languages | ${changes.metadata?.languagesCount?.before || 0} | ${changes.metadata?.languagesCount?.after || 0} | ${this._formatChange(changes.metadata?.languagesCount?.change || 0)} | - |\n`,
      `| Frameworks | ${changes.metadata?.frameworksCount?.before || 0} | ${changes.metadata?.frameworksCount?.after || 0} | ${this._formatChange(changes.metadata?.frameworksCount?.change || 0)} | - |\n\n`,

      this._generateLanguageSection(changes.languages),
      this._generateFrameworkSection(changes.frameworks),
      this._generateDependencySection(changes.dependencies),
      this._generateArchitectureSection(changes.architecture),
      this._generateMetricsSection(changes.metrics),
      this._generateRecommendationsSection(impact),

      '---\n',
      '*Diff report generated by AIOX Diff Engine — Story 2.2*\n',
    ];

    const markdown = sections.join('');
    await fs.writeFile(reportPath, markdown);
    return reportPath;
  }

  /**
   * Generate impact report
   * @private
   */
  async _generateImpact(impact) {
    const reportPath = path.join(this.outputPath, 'impact.json');

    const report = {
      timestamp: new Date().toISOString(),
      overallScore: impact.overallScore,
      severity: impact.severity,
      breaking: impact.breaking,
      components: impact.components,
      recommendations: impact.recommendations,
    };

    await fs.writeFile(reportPath, JSON.stringify(report, null, 2));
    return reportPath;
  }

  /**
   * Generate language section
   * @private
   */
  _generateLanguageSection(langDiff) {
    if (!langDiff) return '';

    let section = '## 🌐 Language Changes\n\n';

    if (langDiff.added?.length > 0) {
      section += '### Added Languages\n';
      langDiff.added.forEach((lang) => {
        section += `- ➕ **${lang.language}**: ${lang.files} files, ${lang.loc} LOC (${lang.percentage}%)\n`;
      });
      section += '\n';
    }

    if (langDiff.removed?.length > 0) {
      section += '### Removed Languages\n';
      langDiff.removed.forEach((lang) => {
        section += `- ➖ **${lang.language}**: ${lang.files} files, ${lang.loc} LOC\n`;
      });
      section += '\n';
    }

    if (langDiff.modified?.length > 0) {
      section += '### Modified Languages\n';
      langDiff.modified.forEach((lang) => {
        const fileDir = lang.fileChange > 0 ? '📈' : '📉';
        section += `- ${fileDir} **${lang.language}**: ${lang.before.files} → ${lang.after.files} files (${this._formatChange(lang.fileChange)})\n`;
      });
      section += '\n';
    }

    return section;
  }

  /**
   * Generate frameworks section
   * @private
   */
  _generateFrameworkSection(fwDiff) {
    if (!fwDiff || (!fwDiff.added?.length && !fwDiff.removed?.length)) return '';

    let section = '## 🛠️ Framework Changes\n\n';

    if (fwDiff.added?.length > 0) {
      section += '### Added\n';
      fwDiff.added.forEach((fw) => {
        section += `- ➕ ${fw}\n`;
      });
      section += '\n';
    }

    if (fwDiff.removed?.length > 0) {
      section += '### Removed\n';
      fwDiff.removed.forEach((fw) => {
        section += `- ➖ ${fw}\n`;
      });
      section += '\n';
    }

    return section;
  }

  /**
   * Generate dependencies section
   * @private
   */
  _generateDependencySection(depDiff) {
    if (!depDiff) return '';

    let section = '## 📦 Dependency Changes\n\n';

    section += `**Total Dependencies:** ${depDiff.total?.before || 0} → ${depDiff.total?.after || 0} (${this._formatChange(depDiff.total?.change || 0)})\n\n`;

    if (depDiff.production?.added?.length > 0) {
      section += `### Production Additions (${depDiff.production.added.length})\n`;
      depDiff.production.added.slice(0, 5).forEach((dep) => {
        section += `- ➕ ${dep}\n`;
      });
      if (depDiff.production.added.length > 5) {
        section += `- ... and ${depDiff.production.added.length - 5} more\n`;
      }
      section += '\n';
    }

    if (depDiff.production?.removed?.length > 0) {
      section += `### Production Removals (${depDiff.production.removed.length})\n`;
      depDiff.production.removed.forEach((dep) => {
        section += `- ➖ ${dep}\n`;
      });
      section += '\n';
    }

    if (depDiff.production?.upgraded?.length > 0) {
      section += `### Production Upgrades (${depDiff.production.upgraded.length})\n`;
      depDiff.production.upgraded.slice(0, 5).forEach((upg) => {
        section += `- 🔄 ${upg.name}: ${upg.from} → ${upg.to}\n`;
      });
      if (depDiff.production.upgraded.length > 5) {
        section += `- ... and ${depDiff.production.upgraded.length - 5} more\n`;
      }
      section += '\n';
    }

    return section;
  }

  /**
   * Generate architecture section
   * @private
   */
  _generateArchitectureSection(archDiff) {
    if (!archDiff) return '';

    let section = '## 🏗️ Architecture Changes\n\n';

    if (archDiff.changed) {
      section += `**Pattern Shift:** ${archDiff.before.name} → ${archDiff.after.name}\n\n`;
    } else {
      section += `**Pattern (Unchanged):** ${archDiff.before.name}\n\n`;
    }

    section += `**Confidence:** ${Math.round(archDiff.after.confidence * 100)}%\n\n`;

    return section;
  }

  /**
   * Generate metrics section
   * @private
   */
  _generateMetricsSection(metricsDiff) {
    if (!metricsDiff) return '';

    let section = '## 📊 Metrics Changes\n\n';
    section += '| Metric | Before | After | Change | Status |\n';
    section += '|--------|--------|-------|--------|--------|\n';

    for (const [key, metric] of Object.entries(metricsDiff)) {
      const statusBadge = metric.status === 'improved' ? '✅' :
                         metric.status === 'regressed' ? '❌' : '➡️';
      const changeStr = metric.change !== undefined
        ? `${metric.change >= 0 ? '+' : ''}${metric.change}`
        : (metric.before === metric.after ? 'no change' : 'changed');

      section += `| ${key} | ${metric.before} | ${metric.after} | ${changeStr} | ${statusBadge} |\n`;
    }

    section += '\n';
    return section;
  }

  /**
   * Generate recommendations section
   * @private
   */
  _generateRecommendationsSection(impact) {
    let section = '## 💡 Recommendations\n\n';

    impact.recommendations.forEach((rec) => {
      const icon = rec.priority === 'critical' ? '🔴' :
                   rec.priority === 'high' ? '🟠' :
                   rec.priority === 'medium' ? '🟡' : '🟢';

      section += `${icon} **[${rec.category}]** ${rec.message}\n`;
    });

    section += '\n';
    return section;
  }

  /**
   * Helper: Severity badge
   * @private
   */
  _severityBadge(severity) {
    const badges = {
      critical: '🔴 Critical',
      high: '🟠 High',
      moderate: '🟡 Moderate',
      low: '🟢 Low',
      minimal: '✅ Minimal',
    };
    return badges[severity] || 'Unknown';
  }

  /**
   * Helper: Format change indicator
   * @private
   */
  _formatChange(num) {
    if (num === 0) return '0';
    return `${num > 0 ? '+' : ''}${num}`;
  }
}

module.exports = { DiffReporter };
